def main(top_block_cls=GFSK_Metrics_test, options=None):

    qapp = Qt.QApplication(sys.argv)

    tb = top_block_cls()

    tb.start()
    tb.flowgraph_started.set()

    tb.show()

    def sig_handler(sig=None, frame=None):
        tb.stop()
        tb.wait()

        Qt.QApplication.quit()
    

    signal.signal(signal.SIGINT, sig_handler)
    signal.signal(signal.SIGTERM, sig_handler)

    timer = Qt.QTimer()
    timer.start(500)
    timer.timeout.connect(lambda: None)

    # define metrics logger call to log metrics periodically
    def update_metrics():
        tb.metrics_logger.log_packet_outcome(success=True, ber=0.0)

    metrics_timer = Qt.QTimer()
    metrics_timer.timeout.connect(update_metrics)
    metrics_timer.start(1000)

    qapp.exec_()

if __name__ == '__main__':
    main()
